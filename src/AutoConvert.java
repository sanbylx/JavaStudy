public class AutoConvert {
    public static void main(String[] args){

        int num ='a';
        System.out.println(num);

        double d1= 80;
        System.out.println(d1);

        int n1=10;
        double d2 = n1 +1.1;
        float d3 = n1+1.1f;

        System.out.println(d2+d3);
        byte b1=10;
        System.out.println(b1);

        int n2=(int)1.9;
        System.out.println(n2);

        int n3= 2000;
        byte b2=(byte)n3;
        System.out.println(b2);




    }
}
