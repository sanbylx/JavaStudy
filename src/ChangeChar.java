public class ChangeChar {
    public static void main(String[] args){

//\t  :一个制表位，实现对齐
        System.out.println("北京\t上海\t天津");
//\n : 换行符
        System.out.println("北京\n上海\n天津");
//\\\ :一个\
        System.out.println("北京\\上海\\天津");
//\\" :一个"
        System.out.println("北京\"上海\"天津");
//\\' :一个'
        System.out.println("北京\'上海\'天津");
//\r:一个回车
        System.out.println("北京上海\r天津");
//
        System.out.println("北京上海\r\n天津");
    }
}
